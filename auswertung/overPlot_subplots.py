plt.rc('text', usetex=True)
plt.rc('font', family='serif')

plt.figure(1)
f, axarr = plt.subplots(2, sharex=True)
axarr[0].hist(p0, bins=401)
axarr[1].hist(p_2_0, bins=401)
plt.xlabel(r"$B^z$[$J_q$]")
plt.ylabel("Anzahl")
axarr[0].grid()
axarr[1].grid()
plt.savefig("./plots/10_10_1.pdf")

